const LANGS: Lang[] = ["ru", "en", "hy"];
const DEFAULT_LANG: Lang = "hy";
const STORAGE_KEY = "smeg.lang";

function readStoredLang(): Lang {
  if (typeof window === "undefined") return DEFAULT_LANG;
  const stored = localStorage.getItem(STORAGE_KEY) as Lang | null;
  return stored && LANGS.includes(stored) ? stored : DEFAULT_LANG;
}

function isCyrillic(text: string): boolean {
  return /[\u0400-\u04FF]/.test(text);
}

type I18nCtx = { lang: Lang; setLang: (l: Lang) => void; t: (key: string, vars?: Record<string, string | number>) => string };
const Ctx = createContext<I18nCtx | null>(null);

export function getI18nDefaults(): Record<Lang, Dict> {
  return dicts;
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(readStoredLang);
  const [overlay, setOverlay] = useState<Record<Lang, Dict>>({ ru: {}, en: {}, hy: {} });
  useEffect(() => {
    if (typeof document !== "undefined") document.documentElement.lang = lang;
  }, [lang]);
  useEffect(() => {
    let cancelled = false;
    (async () => {
      const { data } = await supabase.from("site_content").select("key,value");
      if (cancelled || !data) return;
      const next: Record<Lang, Dict> = { ru: {}, en: {}, hy: {} };
      let stylesMap: ContentStylesMap = {};
      for (const row of data) {
        if (row.key === "__styles__") {
          stylesMap = (row.value as ContentStylesMap) ?? {};
          continue;
        }
        const value = (row.value ?? {}) as Record<string, Partial<Record<Lang, string>>>;
        for (const [k, perLang] of Object.entries(value)) {
          if (!perLang || typeof perLang !== "object") continue;
          (["ru", "en", "hy"] as Lang[]).forEach((l) => {
            const v = perLang[l];
            if (typeof v === "string" && v.trim()) next[l][k] = v;
          });
        }
      }
      setOverlay(next);
      if (typeof document !== "undefined") {
        const css = stylesToCss(stylesMap);
        let styleEl = document.getElementById("ck-runtime-styles") as HTMLStyleElement | null;
        if (!styleEl) {
          styleEl = document.createElement("style");
          styleEl.id = "ck-runtime-styles";
          document.head.appendChild(styleEl);
        }
        styleEl.textContent = css;
        const fonts = collectGoogleFonts(stylesMap);
        document.querySelectorAll('link[data-ck-font="1"]').forEach((n) => n.remove());
        for (const f of fonts) {
          const link = document.createElement("link");
          link.rel = "stylesheet";
          link.href = `https://fonts.googleapis.com/css2?family=${f}&display=swap`;
          link.setAttribute("data-ck-font", "1");
          document.head.appendChild(link);
        }
      }
    })();
    return () => { cancelled = true; };
  }, []);
  const setLang = (l: Lang) => {
    setLangState(l);
    if (typeof window !== "undefined") {
      localStorage.setItem(STORAGE_KEY, l);
      document.documentElement.lang = l;
    }
  };
  const t = (key: string, vars?: Record<string, string | number>) => {
    let s = overlay[lang][key] ?? dicts[lang][key] ?? adminDicts[lang][key] ?? key;
    if (vars) {
      for (const [k, v] of Object.entries(vars)) s = s.replaceAll(`{${k}}`, String(v));
    }
    return s;
  };
  return <Ctx.Provider value={{ lang, setLang, t }}>{children}</Ctx.Provider>;
}

export function useI18n() {
  const v = useContext(Ctx);
  if (!v) throw new Error("useI18n must be used inside I18nProvider");
  return v;
}

/**
 * Pick a localized field with fallback to the base field (RU).
 * Example: pickLocalized(product, "name", "en")
 *   → product.name_en (if present) else product.name
 */
export function pickLocalized(
  obj: Record<string, unknown> | null | undefined,
  base: string,
  lang: Lang,
): string {
  if (!obj) return "";
  const str = (v: unknown) => (typeof v === "string" && v.trim() ? v.trim() : "");
  const ru = str(obj[base]);
  const en = str(obj[`${base}_en`]);
  const hy = str(obj[`${base}_hy`]);

  if (lang === "ru") return ru || en || hy;
  if (lang === "en") return en || hy || (ru && !isCyrillic(ru) ? ru : "");
  // hy: prefer Armenian, then English — avoid Russian fallback on Armenian UI
  return hy || en || (ru && !isCyrillic(ru) ? ru : "");
}

/** Specs JSON localized with fallback. */
export function pickLocalizedSpecs(
  obj: Record<string, unknown> | null | undefined,
  lang: Lang,
): Record<string, string> {
  if (!obj) return {};
  const key = lang === "ru" ? "specs" : `specs_${lang}`;
  const localized = (obj[key] as Record<string, string> | null | undefined) ?? null;
  if (localized && Object.keys(localized).length) return localized;
  if (lang === "hy") {
    const en = (obj.specs_en as Record<string, string> | null | undefined) ?? null;
    if (en && Object.keys(en).length) return en;
  }
  if (lang === "en") {
    const hy = (obj.specs_hy as Record<string, string> | null | undefined) ?? null;
    if (hy && Object.keys(hy).length) return hy;
  }
  return (obj.specs as Record<string, string> | null | undefined) ?? {};
}
